CREATE OR REPLACE TABLE TRANSPORTATION_HUB.HUB.VEHICLE_POSITION (
	VEHICLE_POSITION_UID NUMBER(38,0) NOT NULL DEFAULT TRANSPORTATION_HUB.HUB.VEHICLE_POSITION_SEQ.NEXTVAL,
	VEHICLE_UID NUMBER(38,0),
	DATETIME TIMESTAMP_NTZ(9),
	LATITUDE FLOAT,
	LONGITUDE FLOAT,
	ROUTE_UID NUMBER(38,0),
	TRIP_ID NUMBER(38,0),
	LAST_LOC_ID NUMBER(38,0),
	NEXT_LOC_ID NUMBER(38,0),
	LAST_STOP_SEQ NUMBER(38,0),
	NEXT_STOP_SEQ NUMBER(38,0),
	IN_CONGESTION BOOLEAN,
	OFF_ROUTE BOOLEAN,
	MESSAGE_UID NUMBER(38,0),
	CONSTRAINT PK_VEHICLE_POSITION_ID PRIMARY key (VEHICLE_POSITION_UID),
	CONSTRAINT FK_VEHICLE_ID FOREIGN key (VEHICLE_UID) REFERENCES TRANSPORTATION_HUB.HUB.VEHICLE(VEHICLE_UID),
	CONSTRAINT FK_ROUTE_ID FOREIGN key (ROUTE_UID) REFERENCES TRANSPORTATION_HUB.HUB.ROUTE(ROUTE_UID),
	CONSTRAINT FK_MESSAGE_ID FOREIGN key (MESSAGE_UID) REFERENCES TRANSPORTATION_HUB.HUB.MESSAGE(MESSAGE_UID)
);